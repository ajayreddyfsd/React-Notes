/*
  In React, "state" usually means the component's own internal data
  This data controls how that component renders and behaves
  
  But sometimes, "state" can mean different things:
  1. Component State:
     - Internal data inside one component (this.state or useState based on CC or FC)
     - Controls that component's UI and behavior
  
  2. App State (Global State):
     - Data shared across many components or the whole app
     - Managed with tools like React Context, Redux.
  
  So when someone says "state" in React, they usually mean:
     The component’s internal data that can change and cause re-render
  
  But it’s good to clarify if they mean local component state or global app state!
*/




